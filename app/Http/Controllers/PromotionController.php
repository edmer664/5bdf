<?php

namespace App\Http\Controllers;

use App\Models\Promotion;
use Illuminate\Http\Request;

class PromotionController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        //
        return view(
            'admin.promotions.index',
            [
                'promotions' => Promotion::all()
            ]
        );
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        //
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        // dd($request->all());
        //
        $image = $request->file('image');
        $imageName = time().'.'.$image->getClientOriginalExtension();
        $image->storeAs('public/promotions',$imageName);
        Promotion::create([
            'title' => $request->title,
            'description' => $request->description,
            'branch' => $request->branch,
            'image' => $imageName,
        ]);
        return redirect()->back()->with('success','Promotion created successfully');
    }

    /**
     * Display the specified resource.
     *
     * @param  \App\Models\Promotion  $promotion
     * @return \Illuminate\Http\Response
     */
    public function show(Promotion $promotion)
    {
        //
    }

    /**
     * Show the form for editing the specified resource.
     *
     * @param  \App\Models\Promotion  $promotion
     * @return \Illuminate\Http\Response
     */
    public function edit(Promotion $promotion)
    {
        //
        return view(
            'admin.promotions.edit',
            [
                'promotion' => $promotion
            ]
        );
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  \App\Models\Promotion  $promotion
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, Promotion $promotion)
    {
        //
        $promotion->update([
            'title' => $request->title,
            'description' => $request->description,
            'branch' => $request->branch,
        ]);
        return redirect()->back()->with('success','Promotion updated successfully');
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  \App\Models\Promotion  $promotion
     * @return \Illuminate\Http\Response
     */
    public function destroy(Promotion $promotion)
    {
        //
        // find the image and delete
        $image = $promotion->image;
        $imagePath = public_path('storage/promotions/'.$image);
        unlink($imagePath);
        $promotion->delete();
        return redirect()->back()->with('success','Promotion deleted successfully');
        
    }
}
